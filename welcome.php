        <p>Penyakit kusta, yang disebabkan oleh infeksi bakteri <em>Mycobacterium leprae</em>, merupakan penyakit menular yang biasanya menyerang kulit, sistem saraf, dan selaput lendir. Kusta memiliki prevalensi yang rendah di seluruh dunia, tetapi masih menjadi masalah kesehatan di beberapa negara, terutama di wilayah tropis. Penularan umumnya terjadi melalui droplet pernapasan dari pengidap yang tidak diobati. Masyarakat di sekitar pengidap, terutama mereka yang memiliki kontak dekat, berisiko lebih tinggi terinfeksi. Studi epidemiologi menunjukkan bahwa kusta dapat mempengaruhi orang dari semua usia, tetapi lebih umum terjadi pada orang dewasa.</p>
        <h4>Diagnosa</h4>
            <p>Diagnosa kusta biasanya dilakukan melalui kombinasi wawancara medis, pemeriksaan fisik, dan pengujian laboratorium. Dokter akan mencari tanda-tanda seperti lesi kulit yang tidak terasa atau mati rasa, yang merupakan ciri khas penyakit ini. Tes kulit dan biopsi juga dapat dilakukan untuk mendeteksi keberadaan bakteri <em>Mycobacterium leprae</em>. Penting untuk melakukan diagnosa secepat mungkin agar pengobatan dapat dimulai sebelum terjadi kerusakan saraf yang permanen.</p>
            
            <h4>Klasifikasi</h4>
            <p>Kusta diklasifikasikan menjadi dua bentuk utama: kusta tuberkuloid dan kusta lepromatosa. Kusta tuberkuloid biasanya ditandai dengan satu atau beberapa lesi yang lebih ringan dan kekebalan tubuh yang lebih baik terhadap infeksi. Sementara itu, kusta lepromatosa lebih parah, dapat menyebabkan banyak lesi, kerusakan saraf, dan deformitas. Klasifikasi ini membantu dokter dalam menentukan pengobatan yang tepat dan prognosis pasien.</p>
            
            <h4>Pemeriksaan</h4>
            <p>Pemeriksaan untuk kusta melibatkan penilaian menyeluruh terhadap kondisi kulit dan sistem saraf. Dokter biasanya akan melakukan pemeriksaan fisik untuk mencari tanda-tanda lesi kulit, sensasi, dan fungsi saraf. Selain itu, tes darah mungkin diperlukan untuk mengevaluasi sistem imun dan menyingkirkan penyakit lain. Jika ada kecurigaan kusta, dokter akan merujuk pasien untuk melakukan biopsi kulit guna konfirmasi.</p>

            <h4>Pengobatan</h4>
            <p>Pengobatan kusta dilakukan dengan regimen multi-obat yang terdiri dari antibiotik, seperti rifampicin, dapsone, dan clofazimine. Regimen ini dapat berlangsung dari 6 bulan hingga 2 tahun, tergantung pada jenis kusta. Penting untuk menyelesaikan seluruh pengobatan untuk mencegah resistensi antibiotik dan memastikan kesembuhan total. Pengobatan yang tepat dapat menghentikan penyebaran infeksi dan mencegah kerusakan saraf lebih lanjut.</p>
            
            <h4>Reaksi Kusta</h4>
            <p>Reaksi kusta adalah reaksi imun yang dapat terjadi selama atau setelah pengobatan. Ada dua jenis reaksi yang umum: reaksi tipe 1 (reaksi reversibel) dan reaksi tipe 2 (reaksi leproid). Reaksi ini dapat menyebabkan peradangan pada kulit dan saraf, dan memerlukan penanganan medis segera dengan kortikosteroid untuk mengurangi peradangan dan mencegah kerusakan saraf lebih lanjut.</p>
            
            <h4>Tata Laksana Reaksi</h4>
            <p>Tata laksana reaksi kusta melibatkan penilaian klinis dan pengobatan dengan kortikosteroid. Selain itu, pemantauan yang ketat oleh tenaga medis diperlukan untuk mengevaluasi respon pengobatan dan mengatasi potensi komplikasi. Penting untuk memberikan dukungan psikologis kepada pasien selama fase ini, karena reaksi dapat menyebabkan kecemasan dan stres.</p>
            
            <h4>Kecacatan / Disabilitas</h4>
            <p>Tanpa pengobatan yang tepat, kusta dapat menyebabkan kerusakan saraf permanen yang mengakibatkan kecacatan fisik. Ini termasuk kehilangan kemampuan gerak, deformitas pada tangan dan kaki, serta masalah penglihatan. Disabilitas ini dapat berdampak signifikan pada kualitas hidup pengidap, termasuk stigma sosial dan masalah psikologis. Oleh karena itu, deteksi dini dan pengobatan sangat penting untuk mencegah kecacatan.</p>
            
            <h4>Pencegahan Disabilitas</h4>
            <p>Pencegahan disabilitas akibat kusta melibatkan pendekatan proaktif, seperti deteksi dini, pendidikan masyarakat, dan akses cepat ke layanan kesehatan. Masyarakat harus diinformasikan tentang tanda dan gejala kusta untuk memfasilitasi pemeriksaan dini. Selain itu, tenaga kesehatan perlu dilatih untuk mengenali kusta dan memberikan pengobatan yang tepat untuk mencegah kerusakan saraf.</p>
            
            <h4>Perawatan Diri</h4>
            <p>Pengidap kusta disarankan untuk menjaga kebersihan diri dan melakukan perawatan pada lesi kulit. Ini termasuk menjaga kebersihan area yang terkena, menghindari trauma pada kulit, dan mengikuti instruksi medis untuk perawatan lanjutan. Dukungan dari keluarga dan komunitas juga sangat penting untuk membantu pengidap dalam mengatasi tantangan emosional dan sosial yang mungkin mereka hadapi.</p>
            
